#pragma once

/**
 * @brief Add two numbers
 * 
 * @param first_number 
 * @param second_number 
 * @return int 
 */
int add(int first_number, int second_number);

/**
 * @brief  Substract second number from first_number
 * 
 * @param first_number 
 * @param second_number 
 * @return int 
 */
int substract(int first_number, int second_number);

/**
 * @brief Multiplu two numbers
 * 
 * @param first_number 
 * @param second_number 
 * @return int 
 */
int multiply(int first_number, int second_number);

/**
 * @brief Devide first number from second number
 * 
 * @param first_number 
 * @param second_number 
 * @return double 
 */
double devide(int first_number, int second_number);
